import turtle
turtle.penup()
turtle.goto(-50,0)
turtle.pendown()
turtle.forward(100)
turtle.left(90)
turtle.forward(50)
turtle.left(90)
turtle.forward(100)
turtle.left(90)
turtle.forward(100)
turtle.left(90)
turtle.forward(100)
turtle.left(90)
turtle.forward(100)


turtle.penup()
turtle.goto(0,50)
turtle.pendown()
turtle.left(180)
turtle.forward(100)
turtle.back(50)



turtle.done()